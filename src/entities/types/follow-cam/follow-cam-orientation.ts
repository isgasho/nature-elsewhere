/** The position relative the camera's bounding rectangle. */
export enum FollowCamOrientation {
  /** Top-center. */
  NORTH = 'north',
  /** Top-right. */
  NORTH_EAST = 'northEast',
  /** Mid-right. */
  EAST = 'east',
  /** Bottom-right. */
  SOUTH_EAST = 'southEast',
  /** Bottom-center. */
  SOUTH = 'south',
  /** Bottom-left. */
  SOUTH_WEST = 'southWest',
  /** Mid-left. */
  WEST = 'west',
  /** Top-left. */
  NORTH_WEST = 'northWest',
  CENTER = 'center'
}
